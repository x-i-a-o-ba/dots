$editorRsp = if (Test-Path $editorRspPath) { Parse-RspFile $editorRspPath } else { $null }

$gameProject = New-UnityCsproj -AssemblyName 'Assembly-CSharp' -OutputPath 'Temp\Bin\Debug\Assembly-CSharp\' -Rsp $gameRsp
