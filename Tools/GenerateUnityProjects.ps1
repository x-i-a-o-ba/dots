    $editorProject = New-UnityCsproj -AssemblyName 'Assembly-CSharp-Editor' -OutputPath 'Temp\Bin\Debug\Assembly-CSharp-Editor\' -Rsp $editorRsp -ProjectReferences @('Assembly-CSharp.csproj')
    $projects += $editorProject
}

